    public static String PostMethodForInStream(String url, InputStream ins, String fileName, String... params) {
        // 创建HttpClient
        CloseableHttpClient httpClient = HttpClients.createDefault();
        CloseableHttpResponse response = null;
        String resultString = "";
        try {
            // 创建Http Post请求
            HttpPost httpPost = new HttpPost(url);
            MultipartEntityBuilder builder = MultipartEntityBuilder.create().setMode(HttpMultipartMode.RFC6532);
            /* 绑定文件参数，传入文件流和contenttype，此处也可以继续添加其他formdata参数 */
            builder.addBinaryBody("file", ins, ContentType.MULTIPART_FORM_DATA, fileName);
            for (String str : params) {
                if (str.split("=").length == 2) {
                    builder.addTextBody(str.split("=")[0], str.split("=")[1]); // 附加需要的字段
                }
            }
            HttpEntity entity = builder.build();
            httpPost.setEntity(entity);
            // 执行http请求
            response = httpClient.execute(httpPost);
            resultString = EntityUtils.toString(response.getEntity());// 返回json格式字符串
        } catch (IOException e) {
            e.printStackTrace();
            System.out.println("发送文件服务异常:" + e.getMessage());
        } finally {
            try {
                response.close();
            } catch (IOException e) {
                e.printStackTrace();
            }
        }
        return resultString;
    }

